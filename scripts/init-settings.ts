import { query } from '../src/lib/db';

async function main() {
  try {
    console.log('Creating settings table...');
    await query(`
      CREATE TABLE IF NOT EXISTS settings (
        key TEXT PRIMARY KEY,
        value TEXT NOT NULL,
        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
      );
    `);

    console.log('Inserting default settings...');
    await query(`
      INSERT INTO settings (key, value) VALUES 
      ('system_prompt', 'Ты — копирайтер для Telegram-канала.\nТвоя задача: переписать пост максимально человечным языком.\n\nTone of Voice:\n- Пиши как живой человек, не как бот\n- Разговорный стиль, но без мусорных слов\n- Кратко и по делу, без воды\n- Без канцеляризмов и штампов\n\nСтруктура поста:\n1. Главная мысль/факт (1-2 предложения)\n2. Суть новости/контента (коротко)\n3. Минимальный вывод (1 предложение максимум)\n\nКАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО:\n- Вопросы в конце поста (типа "А что думаете?", "Согласны?")\n- Призывы к действию ("Подписывайтесь", "Делитесь")\n- Много эмодзи (максимум 1-2 на весь пост)\n- Восклицательные предложения подряд\n- Вводные фразы типа "Интересно, что...", "Важно отметить..."\n- Длинные посты - максимум 500 символов\n\nВАЖНО: \n- Заголовок будет на картинке, НЕ дублируй его в тексте\n- Пиши так, как написал бы умный друг в личку\n- Если нечего добавить - не добавляй'),
      ('selection_prompt', 'Проанализируй эти новости и выбери 5 самых интересных и актуальных для канала о технологиях и инновациях. Выбирай те, которые имеют наибольший охват и вовлеченность. Верни только ID новостей через запятую.'),
      ('image_prompt', 'Ты генерируешь промпты для создания фоновых изображений.\nСоздай короткий промпт (до 50 слов) для абстрактного фона, связанного с темой.\nФон должен быть минималистичным, современным, с градиентами или геометрическими формами.\nНЕ включай текст, людей или конкретные объекты — только абстрактный фон.')
      ON CONFLICT (key) DO NOTHING;
    `);

    console.log('Settings initialized successfully');
    process.exit(0);
  } catch (err) {
    console.error('Failed to initialize settings:', err);
    process.exit(1);
  }
}

main();
